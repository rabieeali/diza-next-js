<h2>داستان دیزا</h2>
<p>
مجموعه دیزا امکان همبستگی میان طراحان مستقل ایرانی و علاقمندان به لایف استایل منحصر بفرد خودشان، اعم از لباس ‌و زیورآلات خاص است.
حضور دیزا به عنوان بستری اجابت کننده در ضمینه عرضه و تقاضا عمل می‌کند میان طراحان مستقل تامین کننده و خریداران. 
پلتفرمی آنلاین در زمینه مد و طراحی که خریداران را با تمام طراحان شایسته و برند‌های متعهد ایرانی از طریق ویترینی مجازی متصل می کند و تجربه‌ی فوق العاده هویت یکپارچه‌ای را برای کل ذینفعان شامل دیزا به ارمغان می آورد.
</p>
